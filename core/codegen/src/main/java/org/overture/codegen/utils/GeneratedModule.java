package org.overture.codegen.utils;

public class GeneratedModule
{
	private String name;
	private String content;
	public GeneratedModule(String name, String content)
	{
		super();
		this.name = name;
		this.content = content;
	}
	public String getName()
	{
		return name;
	}
	public String getContent()
	{
		return content;
	}
}
