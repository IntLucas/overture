public class AutomaicProofSystemException extends Exception {

	private static final long serialVersionUID = 3678175716432106405L;

	public AutomaicProofSystemException() {
	}

	public AutomaicProofSystemException(String message) {
		super(message);
	}

	public AutomaicProofSystemException(Throwable cause) {
		super(cause);
	}

	public AutomaicProofSystemException(String message, Throwable cause) {
		super(message, cause);
	}

}
