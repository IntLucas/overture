package org.overturetool.potrans.external_tools;

public class PogProcessorException extends Exception {

	private static final long serialVersionUID = -2295733600468279530L;

	public PogProcessorException() {
	}

	public PogProcessorException(String message) {
		super(message);
	}

	public PogProcessorException(Throwable cause) {
		super(cause);
	}

	public PogProcessorException(String message, Throwable cause) {
		super(message, cause);
	}

}
